              <!-- DISTRIBUTION CAMP DÉPLACÉS -->
              <div class="panel">
                <div class="panel-title"> <a class="collapsed" data-parent="#accordion1" data-toggle="collapse" href="#accordion12" aria-expanded="false"> <span class="open-sub"></span> <strong>DISTRIBUTION CAMP DÉPLACÉS</strong></a> </div>
                <div id="accordion12" class="panel-collapse collapse" role="tablist" aria-expanded="false" style="height: 0px;">
                  <div class="panel-content">
                    <p>Insécurité, violences, guerres et dérèglement climatique forcent des populations à se déplacer, parfois massivement, pour trouver refuge dans des lieux sûrs. La plupart du temps, elles s'installent dans des camps de fortune, dans des conditions précaires, où elles ont besoin d'une aide d'urgence, incluant des soins médicaux ainsi que des distributions d'eau, de nourriture et de produits de première nécessité.</p>
                    <div style="display: flex; justify-content: space-between; align-items: center">
                      <img src="images/missions/camp-deplace1.jpg" alt="Mission au MALI" style="width: 48%;">
                      <img src="images/missions/camp-deplace2.jpg" alt="Mission au MALI" style="width: 48%;">
                    </div>
                    <ul>
                      La distribution efficace et sécurisée dans un camp de déplacés nécessite une planification minutieuse pour répondre aux besoins des populations vulnérables tout en assurant l'ordre et la sécurité. Voici les étapes clés pour organiser une distribution fiable :
                      <!-- Premier -->
                      <li class="list">
                        <strong>Évaluation des Besoins</strong>
                        <ul>
                          <li><strong>Recensement des bénéficiaires : </strong> Identifier et enregistrer toutes les personnes dans le camp, en tenant compte des groupes vulnérables (femmes enceintes, personnes âgées, enfants non accompagnés, etc.).</li>
                          <li><strong>Analyse des besoins : </strong> Déterminer les types et quantités d'articles nécessaires (nourriture, eau, médicaments, kits d'hygiène, etc.).</li>
                        </ul>
                      </li>

                      <!-- Deuxième -->
                      <li class="list">
                        <strong>Planification Logistique</strong>
                        <ul>
                          <li><strong>Établissement d'un plan de distribution : </strong>Choisir les sites de distribution, déterminer les dates et heures pour éviter les foules et assurer un flux régulier.</li>
                          <li><strong>Mobilisation des ressources : </strong>Préparer et entreposer les articles à distribuer. Organiser les moyens de transport pour les acheminer jusqu'au site.</li>
                          <li><strong>Formation des équipes : </strong>Former les équipes de distribution (volontaires, agents de sécurité, personnel médical) sur les procédures à suivre</li>
                        </ul>
                      </li>

                      <!-- Troisème -->
                      <li class="list">
                        <strong>Sensibilisation et Communication</strong>
                        <ul>
                          <li><strong>Information aux bénéficiaires : </strong>Informer les déplacés sur le processus de distribution (lieux, dates, horaires, articles disponibles).</li>
                          <li><strong>Affichage de listes : </strong>Afficher les listes de bénéficiaires pour que chacun sache à quel moment il doit se présenter.</li>
                        </ul>
                      </li>

                      <!-- Quatrième -->
                      <li class="list">
                        <strong>Mise en Place de la Sécurité</strong>
                        <ul>
                          <li><strong>Sécurisation des lieux : </strong>S'assurer que le site de distribution est sécurisé, avec des points d'entrée et de sortie bien définis, et un contrôle des foules.</li>
                          <li><strong>Coordination avec les autorités locales : </strong>Travailler en collaboration avec les forces de sécurité locales pour prévenir tout incident.</li>
                        </ul>
                      </li>

                      <!-- Cinquième -->
                      <li class="list">
                        <strong>Distribution</strong>
                        <ul>
                          <li><strong>Organisation de la file d'attente : </strong>Mettre en place des files d'attente pour garantir l'ordre et donner la priorité aux groupes vulnérables.</li>
                          <li><strong>Vérification des identités : </strong>Utiliser les listes de bénéficiaires pour vérifier l'identité de chaque personne avant la remise des articles.</li>
                          <li><strong>Remise des articles : </strong>Distribuer les articles de manière organisée, en respectant les quantités prévues et en évitant les doublons.</li>
                        </ul>
                      </li>

                      <!-- Sixième -->
                      <li class="list">
                        <strong>Suivi et Évaluation</strong>
                        <ul>
                          <li><strong>Suivi de la distribution : </strong>Surveiller le déroulement de la distribution pour réagir rapidement en cas de problème.</li>
                          <li><strong>Retour d'expérience : </strong>Recueillir les avis des bénéficiaires et des équipes pour identifier les points à améliorer pour les distributions futures.</li>
                        </ul>
                      </li>

                      <!-- Septième -->
                      <li class="list">
                        <strong>Rapports et Documentation</strong>
                        <ul>
                          <li><strong>Documentation : </strong>Tenir des registres détaillés de la distribution, incluant les quantités distribuées, les personnes servies, et tout incident survenu.</li>
                          <li><strong>Rapport de clôture : </strong>Rédiger un rapport final pour les partenaires et les bailleurs de fonds, en incluant des recommandations pour les prochaines actions.</li>
                        </ul>
                      </li>
                      <p style="margin: 20px 0 0;">Ces étapes garantissent que les distributions se déroulent de manière ordonnée, équitable et sécurisée, répondant aux besoins des populations déplacées tout en minimisant les risques et les conflits.</p>
                    </ul>

