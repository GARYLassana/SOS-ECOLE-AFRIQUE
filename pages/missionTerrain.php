                <div class="panel-title"> <a data-parent="#accordion1" data-toggle="collapse" href="#accordion13" class="collapsed" aria-expanded="false"> <span class="open-sub"></span> <strong>CONSTRUCTION (PUITS VILLAGE)</strong></a> </div>
                <div id="accordion13" class="panel-collapse collapse" role="tablist" aria-expanded="false">
                  <div class="panel-content">
                    <p>Malgré le fait que l'accès à l'eau potable et à l'assainissement soit reconnu comme un droit de l'Homme depuis 2010, 300 000 enfants de moins de 5 ans meurent chaque année à cause d'une eau et d'un environnement insalubre. Les femmes marchent 6 heures pour trouver de l'eau. Des tonnes de matières fécales sont déposées dans la nature et les rivières, là où s'abreuvent les villageois et les animaux. Un seul gramme contient jusqu'à 10 000 virus, dont la poliomyélite, et un million de bactéries responsables de la dysenterie, de la diarrhée ou du choléra, des maladies dont il est impensable de mourir encore aujourd'hui. On estime que l'accès systématique à l'eau, à des toilettes et au lavage des mains permettrait de sauver des centaines de milliers de vies chaque année.</p>
                    <div style="display: flex; justify-content: space-between; align-items: center">
                      <img src="images/missions/puits1.jpg" alt="Mission au MALI" style="width: 48%;">
                      <img src="images/missions/puits2.jpg" alt="Mission au MALI" style="width: 48%;">
                    </div>

                    <ul>
                      <li class="list">Le forage d'eau est crucial pour assurer un accès durable à <strong>l'eau potable</strong>, surtout dans les régions où les ressources en surface sont limitées ou contaminées. Contrairement aux puits traditionnels, qui ne pénètrent souvent que les couches supérieures du sol, <strong>le forage permet d'atteindre des nappes phréatiques plus profondes</strong>, où l'eau est souvent plus abondante et moins susceptible d'être affectée par la sécheresse ou la pollution.</li>
                      <li class="list">Cette technique assure une source d'eau fiable tout au long de l'année, contribuant ainsi à <strong>la sécurité alimentaire, à l'hygiène et à la santé publique</strong>. En permettant un accès régulier à de <strong>l'eau propre, le forage réduit les risques de maladies liées à l'eau insalubre, comme le choléra et la diarrhée, et diminue le temps que les populations, notamment les femmes et les enfants, doivent consacrer à la recherche de l'eau, ce qui a des effets positifs sur l'éducation et le développement économique.</strong></li>
                      <li class="list">En somme, <strong>le forage d'eau est un pilier essentiel pour le développement durable</strong>, en garantissant un accès sécurisé à une ressource vitale, indépendamment des conditions climatiques.</li>
                    </ul>
