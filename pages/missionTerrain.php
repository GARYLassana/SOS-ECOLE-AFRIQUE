              <!-- FORAGE D'EAU -->
              <div class="panel">
                <div class="panel-title"> <a data-parent="#accordion1" data-toggle="collapse" href="#accordion14" class="collapsed" aria-expanded="false"> <span class="open-sub"></span> <strong>FORAGE D'EAU</strong></a> </div>
                <div id="accordion14" class="panel-collapse collapse" role="tablist" aria-expanded="false">
                  <div class="panel-content">
                    <p>Un forage d'eau implique l'utilisation de foreuses rotatives pour percer le sol et atteindre des couches d'eau souterraine plus profondes que celles des puits creusés à la main, permettant ainsi l'accès à des réserves et à des débits d'eau plus importants. Les puits d'eau et les forages présentent des différences significatives en termes de fonctionnement et d'utilisation. Le principal avantage du forage réside dans la stabilité de la disponibilité de l'eau tout au long de l'année, quelle que soit la saison ou le climat (même en période de sécheresse extrême), grâce à l'abondance en eau, car il peut atteindre jusqu'à 300 mètres de profondeur. Le forage d'eau est donc synonyme de source d'eau sûre et viable, offrant plus de sécurité qu'un puits traditionnel. Il a un impact majeur sur l'éducation, l'hygiène, l'alimentation, et surtout sur la prévention des maladies telles que le choléra, la diarrhée, et les infections virales.</p>
