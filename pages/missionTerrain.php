                <div class="panel-title"> <a data-parent="#accordion1" data-toggle="collapse" href="#accordion15" class="collapsed" aria-expanded="false"> <span class="open-sub"></span> <strong>LA FAMINE ( enfant gros ventre : pourquoi ? )</strong></a> </div>
                <div id="accordion15" class="panel-collapse collapse" role="tablist" aria-expanded="false">
                  <div class="panel-content">
                    <p>« Lorsque les muscles abdominaux ne peuvent plus supporter le poids des viscères, l'infiltration des liquides sanguins dans l'estomac, causée par un manque de protéines dans l'alimentation, provoque une forte augmentation de la rétention d'eau et une distension sévère du foie. »</p>
                    <p>Les mauvaises conditions de vie des enfants sont les principales causes de la malnutrition en Afrique. Les facteurs de la malnutrition sont multiples : les conflits (intercommunautaires et groupes armés), le manque d'accès à l'eau potable, la pauvreté liée aux chocs économiques et aux inégalités, sont autant d'éléments qui entraînent l'insécurité alimentaire dans les populations vulnérables. Près d'un million d'enfants de moins de 5 ans souffrent de malnutrition aiguë sévère en 2023.</p>
                    <p>Une nouvelle expansion des programmes d'alimentation scolaire pourrait ainsi profiter à des millions d'enfants en leur offrant un accès régulier à des aliments sains et nourrissants.</p>
                  </div>

                  <div style="display: flex; justify-content: space-between; align-items: center">
                    <img src="images/missions/famine3.webp" alt="Mission au MALI" style="width: 48%;">
                    <img src="images/missions/famine2.webp" alt="Mission au MALI" style="width: 48%;">
                  </div>

                  <ul>
                    La gestion de la famine est un défi complexe en raison de divers facteurs socio-économiques, politiques et climatiques. Voici un aperçu de la situation et des efforts de gestion de la famine dans ces pays:
                    <!-- Premier -->
                    <li class="list">
                      <strong>Contexte et Causes de la Famine : </strong>
                      <ul>
                        <li><strong>Changements climatiques : </strong>Les pays situé dans la région sahélienne, sont particulièrement vulnérables aux sécheresses, qui ont un impact direct sur l'agriculture, principale source de subsistance pour une grande partie de la population.</li>
                        <li><strong>Conflits armés : </strong>Les conflits dans le nord et le centre des pays, impliquant des groupes armés et des forces gouvernementales, ont entraîné des déplacements massifs de populations, perturbant l'accès à la nourriture et les activités agricoles.</li>
                        <li><strong>Pauvreté : </strong>Une grande partie de la population vit en dessous du seuil de pauvreté, ce qui limite leur capacité à acheter de la nourriture, surtout en période de crise.</li>
                      </ul>
                    </li>

                    <!-- Deuxième -->
                    <li class="list">
                      <strong>Réponses et Gestion de la Famine : </strong>
                      <ul>
                        <li><strong>Interventions humanitaires : </strong>Nous intervenons régulièrement pour fournir une aide alimentaire d'urgence, surtout dans les zones les plus touchées par l'insécurité alimentaire.</li>
                        <li><strong>Renforcement de la résilience : </strong>Des efforts sont déployés pour améliorer la résilience des communautés face aux chocs climatiques et aux conflits, par exemple, en promouvant des techniques agricoles adaptées aux conditions arides et en diversifiant les sources de revenus.</li>
                      </ul>
                    </li>

                    <!-- Troisème -->
                    <li class="list">
                      <strong>Défis de la Gestion de la Famine : </strong>
                      <ul>
                        <li><strong>Infrastructures limitées : </strong>Le manque d'infrastructures adéquates, notamment les routes et les systèmes de stockage, rend difficile l'acheminement de l'aide dans les zones reculées.</li>
                        <li><strong>Coordination et gouvernance : </strong>La coordination entre les acteurs humanitaires, le gouvernement, et les organisations locales est parfois déficiente, ce qui peut limiter l'efficacité des réponses.</li>
                        <li><strong>Insécurité persistante : </strong>L'insécurité dans certaines régions rend difficile l'accès humanitaire et compromet la mise en œuvre des projets à long terme.</li>
                      </ul>
                    </li>

                    <!-- Quatrième -->
                    <li class="list">
                      <strong>Perspectives d'Avenir : </strong>
                      <ul>
                        <li><strong>Investissements dans l'agriculture : </strong>Augmenter les investissements dans l'agriculture durable et la gestion des ressources en eau est essentiel pour améliorer la sécurité alimentaire à long terme.</li>
                        <li><strong>Paix et stabilité : </strong>La résolution des conflits internes est cruciale pour permettre un accès équitable aux ressources et stabiliser les communautés vulnérables.</li>
                        <li><strong>Partenariats internationaux : </strong>Le maintien et l'élargissement des partenariats internationaux sont indispensables pour mobiliser les ressources nécessaires à la gestion des crises alimentaires.</li>
                      </ul>
                    </li>
                    <p style="margin: 20px 0 0;">La gestion de la famine au Mali nécessite donc une approche multisectorielle, alliant interventions d'urgence et stratégies de développement à long terme pour renforcer la sécurité alimentaire et la résilience des populations.</p>
                  </ul>

